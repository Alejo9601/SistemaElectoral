    /**
     * Metodo para obetener la informacion del elector.
     *
     * @return
     */
    public Map<String, String> getInfoElector() {

        //Creo el mapa de parametros y coloco los mismos
        Map<String, String> PE = new HashMap();
        PE.put("CLAUSTRO", cmbClaustro.getSelectedItem().toString());
        PE.put("NOMBRE", tfNombres.getText());
        PE.put("APELLIDO", tfApellido.getText());
        PE.put("NRODOC", tfNroDoc.getText());
        PE.put("LEGAJO", tfNroLegajo.getText());

        //Si el elector pertenece al claustro de estudiantes entonces...
        if (cmbClaustro.getSelectedItem() == "ESTUDIANTE") {
            PE.put("NOMBRECARRERA", cmbCarreras.getSelectedItem().toString());
            PE.put("CODIGOCARRERA", tfCodigoCarrera.getText());
            PE.put("LOCALIDAD", tfLocalidad.getText());
            PE.put("ULTREINSCRIPCION", cmbA単oReinscripcion.getSelectedItem().toString());
            PE.put("CANTMATERIAS", cmbMaterias.getSelectedItem().toString());
        }

        return PE; //Retorno el mapa de parametros

    }

    /**
     * Metodo para verificar que los campos no estan vacios.
     *
     * @return
     */
    public boolean camposVacios() {
        boolean bandera = false;

        if (("".equals(tfNombres.getText())) || (tfApellido.getText().equals("")) || (tfNroDoc.getText().equals("")) || (tfNroLegajo.getText().equals(""))) {
            bandera = true;
        }

        if (cmbClaustro.getSelectedItem().toString().equals("ESTUDIANTE")) {
            if (tfLocalidad.getText().equals("")) {
                bandera = true;
            }
        }

        return bandera;
    }

